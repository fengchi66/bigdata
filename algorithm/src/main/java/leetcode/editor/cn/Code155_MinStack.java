  class MinStack {

    // å­˜å‰ç¼€æœ€å°å€¼
    Stack<Integer> stack1;
    // å­˜å…ƒç´ 
    Stack<Integer> stack2;

    public MinStack() {

      stack1 = new Stack<>();
      stack2 = new Stack<>();

    }

    public void push(int val) {
      if (!stack1.isEmpty() && stack1.peek() < val) {
        stack1.push(stack1.peek());
      } else {
        stack1.push(val);
      }

      stack2.push(val);

    }

    public void pop() {
      stack1.pop();
      stack2.pop();
    }

    public int top() {
      return stack2.peek();
    }

    public int getMin() {
      return stack1.peek();
    }
  }

